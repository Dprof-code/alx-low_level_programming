#include "3-calc.h"

/**
 * op_add - adds two numbers, a and b
 * @a: num_1
 * @b: num_2
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - difference between two numbers, a and b
 * @a: num_1
 * @b: num_2
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - product of two numbers, a and b
 * @a: num_1
 * @b: num_2
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division of two numbers, a by b
 * @a: num_1
 * @b: num_2
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder of division of two numbers, a by b
 * @a: num_1
 * @b: num_2
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
