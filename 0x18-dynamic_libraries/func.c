#include "func.h"

/**
 * add - sums two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: result
 */
int add(int a, int b)
{
	return a + b;
}

/**
 * sub - subtraction between two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: result
 */
int sub(int a, int b)
{
	return a - b;
}

/**
 * mul - multiplies two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: result
 */
int mul(int a, int b)
{
	return a * b;
}

/**
 * div - divide two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: result
 */
int div(int a, int b)
{
	return a / b;
}

/**
 * add - finds the remainder of division between two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: result
 */
int mod(int a, int b)
{
	return a % b;
}
